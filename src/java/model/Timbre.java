package model;

public class Timbre {

    private int id_timbre;
    private String nome_timbre;
    private String assunto_timbre;
    private String conteudo_timbre;

    public int getId_timbre() {
        return id_timbre;
    }

    public void setId_timbre(int id_timbre) {
        this.id_timbre = id_timbre;
    }

    public String getNome_timbre() {
        return nome_timbre;
    }

    public void setNome_timbre(String nome_timbre) {
        this.nome_timbre = nome_timbre;
    }

    public String getAssunto_timbre() {
        return assunto_timbre;
    }

    public void setAssunto_timbre(String assunto_timbre) {
        this.assunto_timbre = assunto_timbre;
    }

    public String getConteudo_timbre() {
        return conteudo_timbre;
    }

    public void setConteudo_timbre(String conteudo_timbre) {
        this.conteudo_timbre = conteudo_timbre;
    }
}